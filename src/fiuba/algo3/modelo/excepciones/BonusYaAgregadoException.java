package fiuba.algo3.modelo.excepciones;

public class BonusYaAgregadoException extends RuntimeException {

	private static final long serialVersionUID = 1L;

	public BonusYaAgregadoException() {
		super();
	}

	public BonusYaAgregadoException(String arg0, Throwable arg1) {
		super(arg0, arg1);
	}

	public BonusYaAgregadoException(String arg0) {
		super(arg0);
	}

	public BonusYaAgregadoException(Throwable arg0) {
		super(arg0);
	}
	
}
